
#include <stdlib.h>

/**
 * @brief Converts a decimal place to a binary place.
 * 
 * @param decimal_places The number of decimal places.
 * @return The number of binary places.
 */
size_t decimal_to_binary_places(size_t decimal_places);

/**
 * @brief Converts a hex place to a binary place.
 * 
 * @param hex_places The number of hex places.
 * @return The number of binary places.
 */
size_t hex_to_binary_places(size_t hex_places);
